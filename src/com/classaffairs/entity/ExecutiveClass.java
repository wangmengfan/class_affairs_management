package com.classaffairs.entity;

/**
 * @author Haozhifeng
 *
 */
public class ExecutiveClass {
	public Long executiveClassId;
	public String gradeNo;
	public String schoolNo;
    public String name;
    public String monitorStudentNo;
    public String monitorName;
    public String executiveClassGroup;
	public Long getExecutiveClassId() {
		return executiveClassId;
	}
	public void setExecutiveClassId(Long executiveClassId) {
		this.executiveClassId = executiveClassId;
	}
	public String getGradeNo() {
		return gradeNo;
	}
	public void setGradeNo(String gradeNo) {
		this.gradeNo = gradeNo;
	}
	public String getSchoolNo() {
		return schoolNo;
	}
	public void setSchoolNo(String schoolNo) {
		this.schoolNo = schoolNo;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMonitorStudentNo() {
		return monitorStudentNo;
	}
	public void setMonitorStudentNo(String monitorStudentNo) {
		this.monitorStudentNo = monitorStudentNo;
	}
	public String getMonitorName() {
		return monitorName;
	}
	public void setMonitorName(String monitorName) {
		this.monitorName = monitorName;
	}
	public String getExecutiveClassGroup() {
		return executiveClassGroup;
	}
	public void setExecutiveClassGroup(String executiveClassGroup) {
		this.executiveClassGroup = executiveClassGroup;
	}
}
